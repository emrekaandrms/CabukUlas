DROP VIEW IF EXISTS published_companies_public;
CREATE VIEW published_companies_public AS
SELECT
  c.*,
  cat.name AS category_name,
  cat.icon AS category_icon,
  cat.sort_order AS category_sort_order,
  (
    SELECT ch.channel_type
    FROM contact_channels ch
    WHERE ch.company_id = c.id
      AND ch.status = 'published'
      AND ch.archived_at IS NULL
      AND ch.is_fastest = true
    ORDER BY ch.sort_order ASC, ch.updated_at DESC
    LIMIT 1
  ) AS fastest_channel_type
FROM companies c
LEFT JOIN categories cat ON cat.id = c.category_id
WHERE c.status = 'published'
  AND c.archived_at IS NULL
  AND (cat.id IS NULL OR (cat.status = 'published' AND cat.archived_at IS NULL));

DROP VIEW IF EXISTS published_contact_channels_public;
CREATE VIEW published_contact_channels_public AS
SELECT
  ch.*
FROM contact_channels ch
INNER JOIN companies c ON c.id = ch.company_id
WHERE ch.status = 'published'
  AND ch.archived_at IS NULL
  AND c.status = 'published'
  AND c.archived_at IS NULL;

CREATE OR REPLACE FUNCTION append_audit_log()
RETURNS TRIGGER
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path = public
AS $$
DECLARE
  entity_key TEXT;
  action_name TEXT;
  before_json JSONB;
  after_json JSONB;
BEGIN
  IF TG_OP = 'DELETE' THEN
    entity_key := OLD.id::TEXT;
    action_name := TG_TABLE_NAME || '_deleted';
    before_json := to_jsonb(OLD);
    after_json := NULL;
  ELSIF TG_OP = 'UPDATE' THEN
    entity_key := NEW.id::TEXT;
    action_name := TG_TABLE_NAME || '_updated';
    before_json := to_jsonb(OLD);
    after_json := to_jsonb(NEW);
  ELSE
    entity_key := NEW.id::TEXT;
    action_name := TG_TABLE_NAME || '_created';
    before_json := NULL;
    after_json := to_jsonb(NEW);
  END IF;

  INSERT INTO audit_logs (
    entity_type,
    entity_id,
    action,
    before_state,
    after_state,
    changed_by,
    created_at
  )
  VALUES (
    TG_TABLE_NAME,
    entity_key,
    action_name,
    before_json,
    after_json,
    auth.uid(),
    now()
  );

  RETURN COALESCE(NEW, OLD);
END;
$$;

