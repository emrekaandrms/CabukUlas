DROP VIEW IF EXISTS analytics_product_daily;
CREATE VIEW analytics_product_daily AS
SELECT
  date_trunc('day', occurred_at)::date AS metric_date,
  count(DISTINCT user_pseudo_id) AS unique_users,
  count(DISTINCT session_id) FILTER (WHERE event_name = 'session_started') AS sessions,
  coalesce(avg(duration_seconds) FILTER (WHERE event_name = 'session_ended'), 0)::numeric(10,2) AS average_session_seconds,
  count(*) FILTER (WHERE event_name = 'screen_view') AS screen_views,
  count(*) FILTER (WHERE event_name = 'company_opened') AS company_opens,
  count(*) FILTER (WHERE event_name IN ('contact_channel_clicked', 'cargo_tracking_clicked', 'external_link_opened')) AS channel_taps,
  count(*) FILTER (WHERE event_name = 'search_no_result') AS no_result_searches
FROM product_events
GROUP BY 1
ORDER BY 1 DESC;

DROP VIEW IF EXISTS analytics_top_companies;
CREATE VIEW analytics_top_companies AS
SELECT
  c.id AS company_id,
  c.name AS company_name,
  count(*) FILTER (WHERE pe.event_name = 'company_opened') AS opens,
  count(*) FILTER (WHERE pe.event_name = 'contact_channel_clicked') AS channel_taps
FROM product_events pe
INNER JOIN companies c ON c.id = pe.company_id
GROUP BY c.id, c.name
HAVING count(*) FILTER (WHERE pe.event_name = 'company_opened') > 0
ORDER BY opens DESC, channel_taps DESC;

DROP VIEW IF EXISTS analytics_top_channels;
CREATE VIEW analytics_top_channels AS
SELECT
  coalesce(channel_type, 'unknown') AS channel_type,
  count(*) AS taps
FROM product_events
WHERE event_name IN ('contact_channel_clicked', 'cargo_tracking_clicked', 'external_link_opened')
GROUP BY 1
ORDER BY 2 DESC;

DROP VIEW IF EXISTS analytics_no_result_searches;
CREATE VIEW analytics_no_result_searches AS
SELECT
  coalesce(nullif(query_text, ''), '(empty)') AS query_text,
  count(*) AS no_result_count
FROM product_events
WHERE event_name = 'search_no_result'
GROUP BY 1
ORDER BY 2 DESC;
